package pix.gdc.com.vo;

public class TisTeam {
	private String event_name;
	private String support_title;
	private String support_tel;
	private String group_name;
	private String first_name;
	private String last_name;
	private String picture;
	private String email;
	private int supports_id;
	
	public String getEvent_name() {
		return event_name;
	}
	public void setEvent_name(String event_name) {
		this.event_name = event_name;
	}
	public String getSupport_title() {
		return support_title;
	}
	public void setSupport_title(String support_title) {
		this.support_title = support_title;
	}
	public String getSupport_tel() {
		return support_tel;
	}
	public void setSupport_tel(String support_tel) {
		this.support_tel = support_tel;
	}
	public String getGroup_name() {
		return group_name;
	}
	public void setGroup_name(String group_name) {
		this.group_name = group_name;
	}
	public String getFirst_name() {
		return first_name;
	}
	public void setFirst_name(String first_name) {
		this.first_name = first_name;
	}
	public String getLast_name() {
		return last_name;
	}
	public void setLast_name(String last_name) {
		this.last_name = last_name;
	}
	public String getPicture() {
		return picture;
	}
	public void setPicture(String picture) {
		this.picture = picture;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public int getSupports_id() {
		return supports_id;
	}
	public void setSupports_id(int supports_id) {
		this.supports_id = supports_id;
	}
	
	
}
