package pix.gdc.com.vo;

public class FestNewsLetterEmail {
	private int news_letter_id;
	private String news_letter_email;
	private String news_letter_ip;
	private String news_letter_date;
	private String para;
	
	public int getNews_letter_id() {
		return news_letter_id;
	}
	public void setNews_letter_id(int news_letter_id) {
		this.news_letter_id = news_letter_id;
	}
	public String getNews_letter_email() {
		return news_letter_email;
	}
	public void setNews_letter_email(String news_letter_email) {
		this.news_letter_email = news_letter_email;
	}
	public String getNews_letter_ip() {
		return news_letter_ip;
	}
	public void setNews_letter_ip(String news_letter_ip) {
		this.news_letter_ip = news_letter_ip;
	}
	public String getNews_letter_date() {
		return news_letter_date;
	}
	public void setNews_letter_date(String news_letter_date) {
		this.news_letter_date = news_letter_date;
	}
	public String getPara() {
		return para;
	}
	public void setPara(String para) {
		this.para = para;
	}
}
